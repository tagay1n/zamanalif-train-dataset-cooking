def reverse_zamanalif_for_review(word: str) -> str:
    """Best-effort test-data reverse transliteration for manual review."""
    result: list[str] = []
    index = 0
    while index < len(word):
        chunk = word[index:]
        if chunk.startswith("şç"):
            result.append("щ")
            index += 2
        elif chunk.startswith("ts"):
            result.append("ц")
            index += 2
        elif chunk.startswith("iä"):
            result.append("ия")
            index += 2
        elif chunk.startswith("iü"):
            result.append("ию")
            index += 2
        elif chunk.startswith(("ya", "yä")):
            result.append("я")
            index += 2
        elif chunk.startswith(("yu", "yü")):
            result.append("ю")
            index += 2
        elif chunk.startswith("yo"):
            result.append("ё")
            index += 2
        elif chunk.startswith(("yı", "ye")):
            result.append("е")
            index += 2
        else:
            result.append(REVERSE_TRANSLITERATION_MAP.get(word[index], word[index]))
            index += 1
    return "".join(result)


